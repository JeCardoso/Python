# Programar em Python #35 - Operadores Bit a Bit

x = 60; # 00111100
y = 13; # 00001101

print('Resultado do operador &:', x & y); # 12 -> 00001100
print('Resultado do operador |:', x | y); # 61 -> 00111101
print('Resultado do operador <<:', x << 2); # 240 -> 11110000
print('Resultado do operador >>:', x >> 2); # 15 -> 00001111